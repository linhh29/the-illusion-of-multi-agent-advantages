subprocess_semaphore = asyncio.Semaphore(200)

async def run_swebench_evaluation(judge_path: str, instance_id: str, extracted_answer: str, technique: str, solution_name, code_snippet=None, file_path=None):
    """
    Run SWE-Bench evaluation using the harness system.
    
    Args:
        judge_path (str): Path to store results and reports (should contain run info, e.g., "exp_evaluation_run1")
        instance_id (str): Instance ID of the problem
        extracted_answer (str): The patch to evaluate
        technique (str): Technique name (e.g., "llmlp", "cot_sc")
        solution_name (str): Solution identifier
        code_snippet (str): Optional code snippet from input
        file_path (str): Path to the dataset file
    
    Returns:
        dict: Dictionary containing 'score' (float) and 'error_instances' (int)
    """
    # Convert judge_path to absolute path for consistent path handling
    judge_path_abs = os.path.abspath(judge_path)
    
    # Extract run identifier from judge_path to ensure unique run_id for each run
    # judge_path format: "exp_name_evaluation_run{N}" or similar
    judge_path_basename = os.path.basename(judge_path.rstrip('/'))
    # Extract run number if present (e.g., "run1", "run2", etc.)
    run_match = re.search(r'run(\d+)', judge_path_basename)
    run_suffix = f"_run{run_match.group(1)}" if run_match else ""
    # If no run number found, use judge_path basename as suffix to ensure uniqueness
    if not run_suffix:
        # Use a hash or simplified version of judge_path to ensure uniqueness
        run_suffix = f"_{judge_path_basename.replace('_evaluation', '')}"
    
    # Normalize and fix patch headers
    computed_header = extract_hunks_and_recalculate_headers(extracted_answer)
    print(f"computed_header: {computed_header}")
    extracted_answer = replace_hunk_headers_with_computed_counts(extracted_answer, computed_header)
    extracted_answer = normalize_diff_string(extracted_answer)
    print(f"updated extracted_answer: {extracted_answer[:200]}...")

    prediction = {
        "instance_id": instance_id,
        "model_patch": extracted_answer,
        "model_name_or_path": "gpt-4o",
    }

    # Use os.path.join for better path handling
    results_dir = os.path.join(judge_path_abs, 'results')
    os.makedirs(results_dir, exist_ok=True)
    path_to_prediction = os.path.join(results_dir, f'_{instance_id}_{solution_name}.json')
    print('path_to_prediction: ', path_to_prediction)

    # Create a list with the prediction
    predictions_list = [prediction]
    
    # Write as a JSON array
    with open(path_to_prediction, 'w') as f:
        json.dump(predictions_list, f, indent=4)

    # Construct the command
    num_workers = 1
    # Include run_suffix in run_id to ensure each run has unique log directories
    run_id = f'{instance_id}_{technique}_{solution_name}{run_suffix}'
    run_id = run_id.replace(' ', '_').replace('-', '_').replace('(', '_').replace(')', '_')
    print('run_id: ', run_id)
    print('judge_path: ', judge_path)
    print('run_suffix: ', run_suffix)

    # Read the report path
    # Use run_id to match the actual filename format generated by swebench
    # Format: gpt-4o.{run_id}.json
    reports_dir = os.path.join(judge_path_abs, 'reports')
    os.makedirs(reports_dir, exist_ok=True)
    result_path = os.path.join(reports_dir, f'gpt-4o.{run_id}.json')
    print('result_path: ', result_path)
    
    # Clean up log directory to avoid conflicts
    # Move logs to judge_path to keep different runs separate
    # swebench harness creates: logs/run_evaluation/{run_id}/{model_name_or_path}/{instance_id}/
    # where model_name_or_path is from pred.get(KEY_MODEL, "None").replace("/", "__")
    # and instance_id is the original instance_id (no replacement, no suffix)
    # We'll run swebench from judge_path directory so logs will be created there
    logs_base_dir = os.path.join(judge_path_abs, 'logs', 'run_evaluation')
    os.makedirs(logs_base_dir, exist_ok=True)
    model_name_or_path = "gpt-4o"  # This matches the model_name_or_path in prediction dict
    log_dir_for_instance = os.path.join(logs_base_dir, run_id, model_name_or_path, instance_id)
    
    # Convert paths to relative paths from judge_path for swebench command
    # Since we'll run swebench from judge_path directory
    path_to_prediction_rel = os.path.relpath(path_to_prediction, judge_path_abs)
    reports_dir_rel = os.path.relpath(reports_dir, judge_path_abs)
    
    cmd = [
        "python", "-m", "swebench.harness.run_evaluation",
        "--dataset_name", file_path,
        "--predictions_path", path_to_prediction_rel,
        "--max_workers", str(num_workers),
        "--run_id", str(run_id),
        "--report_path", reports_dir_rel,
        "--force_rebuild", "False"  # Explicitly disable rebuild to use local images only
    ]
    
    # Set environment variables to ensure we use local images only
    # Note: swebench will still try to pull if image not found locally,
    # but with force_rebuild=False, it will use local images if they exist
    env = os.environ.copy()
    # Add environment variable to indicate we want to use local images only
    # This is a hint for swebench to prefer local images
    env['SWEBENCH_USE_LOCAL_ONLY'] = '1'
    
    async with subprocess_semaphore:
        max_retries = 1
        for attempt in range(max_retries):
            # Delete log directory before each attempt to ensure fresh evaluation
            if os.path.exists(log_dir_for_instance):
                try:
                    shutil.rmtree(log_dir_for_instance)
                    print(f"Attempt {attempt + 1}: Deleted log directory: {log_dir_for_instance}")
                except Exception as del_e:
                    print(f"Warning: Failed to delete log directory {log_dir_for_instance}: {del_e}")
            
            try:
                # Create async subprocess with environment that prevents Docker pulls
                # Run from judge_path directory so logs will be created there
                process = await asyncio.create_subprocess_exec(
                    *cmd,
                    stdout=asyncio.subprocess.PIPE,
                    stderr=asyncio.subprocess.PIPE,
                    env=env,  # Use modified environment
                    cwd=judge_path_abs  # Run from judge_path so logs are created there
                )
                
                # Wait for process to complete
                stdout, stderr = await process.communicate()
                
                if process.returncode == 0:
                    print("Evaluation completed successfully")
                    break
                else:
                    error_msg = stderr.decode() if stderr else "Unknown error"
                    print(f"Error running evaluation (attempt {attempt + 1}/{max_retries}): {error_msg}")
                    if attempt < max_retries - 1:
                        await asyncio.sleep(2)
                    else:
                        print("Max retries reached, returning 0.0 score")
                        return 0.0
            except Exception as e:
                print(f"Exception (attempt {attempt + 1}/{max_retries}): {e}, retrying...")
                if attempt < max_retries - 1:
                    await asyncio.sleep(2)
                else:
                    print("Max retries reached, returning 0.0 score")
                    return 0.0

    # Read evaluation result
    if os.path.exists(result_path):
        with open(result_path, "r") as f:
            data = json.load(f)
        score = float(data.get('resolved_instances', 0))
        error_instances = int(data.get('error_instances', 0))
    else:
        print(f"Report file not found at {result_path}")
        score = 0.0
        error_instances = 1  # Treat missing report as error

    # Clean up docker container to avoid lock
    # Include run_id in container name to ensure uniqueness across runs
    container_suffix = run_id.replace('_', '.').replace('-', '.')
    container_name = f'sweb.eval.{instance_id}.{container_suffix}'
    result = subprocess.run(["docker", "rm", "-f", container_name], capture_output=True, text=True)
    
    if result.returncode == 0:
        print(f"Removed container: {container_name}")
    else:
        # Container might not exist, which is fine
        if "No such container" not in result.stderr:
            print(f"Warning: Failed to remove container {container_name}: {result.stderr}")

    return {'score': score, 'error_instances': error_instances}

def compute_success_percentage(json_path):
    """Compute success percentage from evaluation report."""
    with open(json_path, "r") as f:
        data = json.load(f)

    for instance_id, result in data.items():
        tests_status = result.get("tests_status", {})
        resolved = result.get("resolved")
        assert type(resolved) == bool

        # Collect pass/fail lists for FAIL_TO_PASS and PASS_TO_PASS
        total_tests = 0
        passed_tests = 0

        for category in ["FAIL_TO_PASS", "PASS_TO_PASS"]:
            success = tests_status.get(category, {}).get("success", [])
            failure = tests_status.get(category, {}).get("failure", [])
            total_tests += len(success) + len(failure)
            passed_tests += len(success)

        percentage = (passed_tests / total_tests) * 100 if total_tests > 0 else 0.0

        print(f"{instance_id} → {passed_tests} passed test | {total_tests} total_tests | {passed_tests}/{total_tests} passed → {percentage:.1f}% | resolved: {resolved}")

        if resolved:
            return 1.0, percentage, passed_tests, total_tests
        else:
            return 0.0, percentage, passed_tests, total_tests


def replace_hunk_headers_with_computed_counts(diff_text, hunk_infos):
    """
    Replace all hunk headers in the diff text using computed Y and B values
    from hunk_infos (as returned by extract_all_hunks_and_recalculate_headers).
    Returns the updated diff text.
    """
    lines = diff_text.strip().splitlines()
    hunk_header_re = re.compile(r'^@@ -(\d+),(\d+) \+(\d+),(\d+) @@')

    output_lines = []
    hunk_index = 0

    for line in lines:
        match = hunk_header_re.match(line)
        if match and hunk_index < len(hunk_infos):
            # Get computed values
            info = hunk_infos[hunk_index]
            hunk_index += 1

            original_start = info['original_start']
            new_start = info['new_start']
            computed_Y = info['computed_Y']
            computed_B = info['computed_B']

            new_header = f"@@ -{original_start},{computed_Y} +{new_start},{computed_B} @@"
            output_lines.append(new_header)
        else:
            output_lines.append(line)

    return "\n".join(output_lines)

def extract_hunks_and_recalculate_headers(diff_text):
    """
    Parses a unified diff string, handles multiple hunks for a single file,
    and returns a list of dictionaries for each hunk:
    {
        'original_start': X,
        'new_start': A,
        'declared_Y': Y,
        'declared_B': B,
        'computed_Y': actual_Y,
        'computed_B': actual_B,
        'lines': [...],  # The hunk body
    }
    """
    lines = diff_text.strip().splitlines()
    hunk_header_re = re.compile(r'^@@ -(\d+),(\d+) \+(\d+),(\d+) @@')

    results = []
    current_hunk_lines = []
    current_header = None

    for line in lines:
        match = hunk_header_re.match(line)
        if match:
            # Process previous hunk
            if current_header:
                computed_Y, computed_B = compute_hunk_line_counts(current_hunk_lines)
                results.append({
                    'original_start': current_header[0],
                    'new_start': current_header[1],
                    'declared_Y': current_header[2],
                    'declared_B': current_header[3],
                    'computed_Y': computed_Y,
                    'computed_B': computed_B,
                    'lines': current_hunk_lines
                })
                current_hunk_lines = []

            # Start new hunk
            original_start = int(match.group(1))
            original_len = int(match.group(2))
            new_start = int(match.group(3))
            new_len = int(match.group(4))
            current_header = (original_start, new_start, original_len, new_len)
        elif current_header:
            current_hunk_lines.append(line)

    # Final hunk
    if current_header and current_hunk_lines:
        computed_Y, computed_B = compute_hunk_line_counts(current_hunk_lines)
        results.append({
            'original_start': current_header[0],
            'new_start': current_header[1],
            'declared_Y': current_header[2],
            'declared_B': current_header[3],
            'computed_Y': computed_Y,
            'computed_B': computed_B,
            'lines': current_hunk_lines
        })

    return results

def compute_hunk_line_counts(hunk_lines):
    """
    Given a list of lines in a diff hunk body (excluding the header),
    compute the correct number of lines from the original file (Y)
    and from the new file (B), for the hunk header format:

        @@ -X,Y +A,B @@

    Returns:
        (Y, B): tuple of integers
    """
    num_context = 0
    num_removed = 0
    num_added = 0

    for line in hunk_lines:
        if line.startswith(" "):
            num_context += 1
        elif line.startswith("-"):
            num_removed += 1
        elif line.startswith("+"):
            num_added += 1
        else:
            # Ignore lines like "\ No newline at end of file"
            continue

    y = num_context + num_removed  # lines from original file
    b = num_context + num_added    # lines from new file
    return y, b


def normalize_diff_string(diff_content: str) -> str:
    """Normalize diff string to ensure proper formatting."""
    # Normalize all line endings to \n
    content = diff_content.replace("\r\n", "\n").replace("\r", "\n")
    # Ensure a trailing newline
    if not content.endswith("\n"):
        content += "\n"
        print('Added trailing newline to patch')
    return content


def extract_xml(text: str, tag: str) -> str:
    """
    Extracts the content of the specified XML tag from the given text.
    
    Args:
        text (str): The text containing the XML.
        tag (str): The XML tag to extract content from.
    
    Returns:
        str: The content of the specified XML tag, or an empty string if the tag is not found.
    """
    match = re.search(f'<{tag}>(.*?)</{tag}>', text, re.DOTALL)
    return match.group(1) if match else ""

